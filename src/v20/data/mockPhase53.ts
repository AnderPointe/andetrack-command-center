type KPI = { label: string; value: string | number; sub?: string };
const k = (label: string, value: string | number, sub?: string): KPI => ({ label, value, sub });

type Area = {
  score: string;
  kpis: KPI[];
  matrix: { control: string; status: string; owner: string }[];
  exceptions: { id: string; control: string; desc: string; owner: string }[];
  remediation: { id: string; action: string; owner: string; due: string; status: string }[];
};
const mkArea = (
  score: string,
  kpis: [string, string | number, string?][],
  matrix: [string, string, string][],
  exceptions: [string, string, string, string][] = [],
  remediation: [string, string, string, string, string][] = [],
): Area => ({
  score,
  kpis: kpis.map(([l, v, s]) => k(l, v, s)),
  matrix: matrix.map(([control, status, owner]) => ({ control, status, owner })),
  exceptions: exceptions.map(([id, control, desc, owner]) => ({ id, control, desc, owner })),
  remediation: remediation.map(([id, action, owner, due, status]) => ({ id, action, owner, due, status })),
});

